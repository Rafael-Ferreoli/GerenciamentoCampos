    private void jButton_Cadastro_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Cadastro_EditarActionPerformed
        this.matriculaSelecionada = JOptionPane.showInputDialog("Informe a matrícula do usuário:", "");
        Cadastro cadastroEditando = this.gerente.buscarCadastro(matriculaSelecionada);
        if (cadastroEditando != null) {
            this.editando = true;
            this.limparCampos();
            this.habilitarCampos(true);
            objetoParaCampos(cadastroEditando);

        } else {
            System.out.println("cadastro inexistente");
            JOptionPane.showMessageDialog(this, "A matrícula informada não foi encontrada no sistema.", "Cadastro Inexistente", HEIGHT);
        }
    }//GEN-LAST:event_jButton_Cadastro_EditarActionPerformed

