    public void limparCampos() {
        this.jTextField_Cadastro_Nome.setText("");
        this.jTextField_Cadastro_CPF.setText("");
        this.jTextField_Cadastro_Cargo.setText("");
        this.jTextField_Cadastro_Email.setText("");
        this.jTextField_Cadastro_Matricula.setText("");
        this.jTextField_Cadastro_Senha.setText("");
    }

    public void habilitarCampos(boolean flag) {
        this.jTextField_Cadastro_Nome.setEnabled(flag);
        this.jTextField_Cadastro_CPF.setEnabled(flag);
        this.jTextField_Cadastro_Cargo.setEnabled(flag);
        this.jTextField_Cadastro_Email.setEnabled(flag);
        this.jTextField_Cadastro_Matricula.setEnabled(flag);
        this.jTextField_Cadastro_Senha.setEnabled(flag);
    }

    public Cadastro camposParaObjeto() {
        Cadastro cadastro = new Cadastro();
        cadastro.setNome(jTextField_Cadastro_Nome.getText());
        cadastro.setCargo(jTextField_Cadastro_Cargo.getText());
        cadastro.setCpf(jTextField_Cadastro_CPF.getText());
        cadastro.setEmail(jTextField_Cadastro_Email.getText());
        cadastro.setMatricula(jTextField_Cadastro_Matricula.getText());
        cadastro.setSenha(jTextField_Cadastro_Senha.getText());
        return cadastro;
    }

    public void objetoParaCampos(Cadastro cadastro) {
        jTextField_Cadastro_Nome.setText(cadastro.getNome());
        jTextField_Cadastro_CPF.setText(cadastro.getCpf());
        jTextField_Cadastro_Cargo.setText(cadastro.getCargo());
        jTextField_Cadastro_Email.setText(cadastro.getEmail());
        jTextField_Cadastro_Matricula.setText(cadastro.getMatricula());
        jTextField_Cadastro_Senha.setText(cadastro.getSenha());
    }

